The exterior elevation is much like the foundation layout sheet. While there are dimensions that must be adhered to 
such as the finished floor and plate height and the roof pitch, there are things that can be changed is absolutely necessary.
 Things of a more decorative nature can always be changed or altered to better suit your own specific style. Things like banding 
