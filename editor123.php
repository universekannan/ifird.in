 human elements including different forms of land use, buildings and structures, and transitory elements such as lighting and 
 weather conditions.

Combining both their physical origins and the cultural overlay of human presence, often created over millennia,
 landscapes reflect a living synthesis of people and place that is vital to local and national identity. 
 The character of a landscape helps define the self-image of the people who inhabit it and a sense of place 
 that differentiates one region from other regions. It is the dynamic backdrop to people’s lives. Landscape 
 can be as varied as farmland, a landscape park, or wildernessGeomorphology is the scientific study of the 
 origin and evolution of topographic and bathymetric features created by physical or chemical processes operating
 at or near Earth’s surface. Geomorphologists seek to understand why landscapes look the way they do, to understand 
 landform history and dynamics and to predict changes through a combination of field observations, physical experiments
 and numerical modeling.</p> 
