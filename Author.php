              <h2>AUTHOR(S) GUIDELINE</h2>
            </div>

				
			
				<h3 style="text-align: center;"></h3> 
<p style="text-align: center;"><em>(Authors should submit their papers in MS Word Format, drafted as per the 'Paper Template' available for download above)</em></p>
<ul style="text-align: justify;">
<li>All accepted original research papers in the English Language will be published in selected journals as per the publication policy, as available on the conference website.</li>
<li>Once you receive the Invitation/ Acceptance letter, that means your full paper is also accepted for publication in an International Journal, if you follow the communicated editorial instructions/ guidelines.</li>
<li>The journal publication will be peer-reviewed, checked for plagiarism, index-journaled, archived, open access, referenced by CrossRef and will carry ISSN number and DOI.</li>
<li>Even if your full paper is not yet ready, you may participate in the desired conference with your abstract. The abstract must contain: (<em>Article Title; Full Names/ Emails/ Affiliations of the authors; Abstract in 100-300 words; 3-7 Keywords)</em></li>
<li>You may update your submitted abstract/ title/ co-authors/ submit your full-paper at a later stage (before the conference).</li>
<li>You may submit your full original paper for publication in the conference journal, when it is complete, till the conference date. The last date of submission is the conference day itself.</li>
</ul>
<p style="text-align: justify;"><strong><h4>Paper Submission</h4></strong></p>
<p style="text-align: justify;">While submitting the full paper, please provide the following in the email:</p>
<ul style="text-align: justify;">
<li>Full paper in MS Word format (Ideally 2500-3000 words).</li>
<li>Details of 2 reviewers with their names, affiliations, contact number and email IDs (If possible send two emails for each reviewer).</li>
<li>Duly filled and scanned 'consent to publish form' with a handwritten signature. 'Consent to Publish' form can be downloaded from the 'Quick Downloads' section of the conference website.</li>
<li>Send your paper along with your Conference Registration ID to ifirdjournal@gmail.com</li>
</ul>
<p style="text-align: justify;"><strong>We follow the following steps for publication in our associated International Journals. The publication process takes around 45 days, starting from the end of the conference.</strong></p>
<ul style="text-align: justify;">
<li>A list of registered papers is sent to all the participants of the conference within a week's time after the conference. Please see, if your paper is included in the list. If not, please write back to us for inclusion. This list would also mention for any deficiency/incompleteness found in the submitted paper. You would be given 10 days to return your complete papers/ required information.</li>
<li>After this, the editorial team would send all complete papers for the review (usually 3-5 reviewers). The review process takes around 30 days.</li>
<li>Following this, our editor would send the editorial comments/ suggestions to the corresponding author. Please improve the paper as indicated in the review and send it back to us within 10 days.</li>
<li>If the paper received is complete in all regards as per the comments/ suggestions, it would be sent for final publication, else we would send it again to you and finally, 5 days would be given to you for its improvement.</li>
<li>Finally, the paper is published and the authors are informed about the published paper by email, which contains the paper URL, DOI, Citation, and other related information.</li>
<li>If you fail to meet the deadlines/ correct the paper as per review comments, the paper may be rejected or it will be postponed for publication in the next issue.</li>
<li>Normally, the entire process takes around 45 days.</li>
</ul>
<p style="text-align: justify;"><strong><h4>Withdrawal of Submitted Paper</h4></strong></p>
<p style="text-align: justify;">Authors may request the conference secretariat for withdrawing their paper, for publishing it elsewhere (in the journal of their choice). In such cases, the requested papers are removed from the publication process. The withdrawal requests may be given to the conference secretariat before the commencement of the publication process (7 days after the conference).</p>
<p style="text-align: justify;"><strong><h4>Plagiarism and Retraction Policy</h4></strong></p>
<p style="text-align: justify;">The ethics statements for Institute of Innovative Research and Development are based on the Committee on Publication Ethics (COPE) Code of Conduct guidelines available at www.ifird.com</p>
<ul style="text-align: justify;">
<li>All accepted papers will be checked for plagiarism using urkund (plagiarism Software). The acceptable plagiarism level is up to 10%. Plagiarism report will be sent to the author if the manuscript is detected with plagiarism levels higher than acceptable limits. The author needs to take corrective measures, so as to, bring the plagiarism below 10%, and resend the manuscript to the editor. The conference secretariat will not publish any paper, which fails in these criteria.</li>
<li>The published papers are also submitted to 'CrossMark Metadata'. CrossMark allows scholars to easily identify instances of documents that are being actively maintained by their publishers. The appearances of a CrossMark logo on the published paper indicates that the publisher is taking care of or stewarding it through any updates, corrections, retractions, or other changes.</li>
<li>Even after publication, if the paper is found/ reported of plagiarism or false science, the author would be sent a 'Retraction Notice', and the paper will be revised/ retracted from the journal (according to the given situation).</li>
</ul>
			
